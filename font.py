FONT_CACHE = {}

def gen_font(text, new_font):
    for q in text:
        if q in _default:
            text = text.replace(q, new_font[_default.index(q)])
    return text

def build_menu():
    keyboard = [
        [
            InlineKeyboardButton("🎲 Random", callback_data="font:random"),
            InlineKeyboardButton("📋 List", callback_data="font:list")
        ]
    ]

    names = list(Fonts.keys())
    for i in range(0, len(names), 4):
        keyboard.append([
            InlineKeyboardButton(n, callback_data=f"font:{n}")
            for n in names[i:i+4]
        ])

    return InlineKeyboardMarkup(keyboard)

def font_cmd(update: Update, context: CallbackContext):
    print("[DEBUG] /font dipanggil")

    text = " ".join(context.args)
    reply = update.message.reply_to_message

    if reply:
        text = reply.text

    if not text:
        return update.message.reply_text("📌 kirim text atau reply pesan dulu.")

    FONT_CACHE[update.effective_chat.id] = text
    print(f"[DEBUG] TEXT DISIMPAN: {text}")

    update.message.reply_text("✨ pilih font:", reply_markup=build_menu())

def font_cb(update: Update, context: CallbackContext):
    query = update.callback_query
    data = query.data
    gid = query.message.chat.id

    print(f"[DEBUG FONT CALLBACK]: {data}")

    if data == "font:back":
        return

    text = FONT_CACHE.get(gid)
    if not text:
        return query.answer("❌ tidak ada text", show_alert=True)

    if data == "font:list":
        return query.edit_message_text(
            "📚 FONT LIST:\n\n" + "\n".join(f"• {f}" for f in Fonts),
            reply_markup=build_menu()
        )

    name = random.choice(list(Fonts)) if data == "font:random" else data.split(":",1)[1]

    if name not in Fonts:
        return query.answer("❌ font tidak ditemukan", show_alert=True)

    result = gen_font(text, Fonts[name])

    # 🔥 KIRIM HASIL LANGSUNG (BIAR BISA DI COPY)
    context.bot.send_message(
        chat_id=gid,
        text=result
    )

    query.answer("✅ jadi")

def font_back(update: Update, context: CallbackContext):
    query = update.callback_query
    print("[DEBUG] BACK DIPENCET")
    query.edit_message_text("✨ pilih font:", reply_markup=build_menu())
    query.answer()

def register_font(dp):
    print("[DEBUG] register_font jalan")

    dp.add_handler(CommandHandler("font", font_cmd))
    dp.add_handler(CallbackQueryHandler(font_back, pattern=r"^font:back$"))
    dp.add_handler(CallbackQueryHandler(font_cb, pattern=r"^font:"))
